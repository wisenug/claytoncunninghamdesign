/* Generate static per-capability pages (capabilities-<slug>.html) from
   templates/capabilities.html + js/projects.js. Each page gets unique meta, a
   pre-rendered (crawlable) project grid, and static filter chips — no
   client-side rendering, so search engines index every tag as a real page.
   Re-run after editing js/projects.js or templates/capabilities.html, then run
   inject-schema.mjs and generate-sitemap.mjs. */
import fs from 'fs/promises';
import sharp from 'sharp';

const SITE = 'https://www.claytoncunninghamdesign.com';

// Load browser-global manifest
const shim = {};
new Function('window', await fs.readFile('js/projects.js', 'utf8'))(shim);
const { CCD_PROJECTS, CCD_TAG_META, CCD_TAG_HREF } = shim;

const template = await fs.readFile('templates/capabilities.html', 'utf8');

const esc = s => String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');

async function dims(src) {
  try {
    const m = await sharp(decodeURI(src), { limitInputPixels: false }).metadata();
    return { w: m.width, h: m.pages && m.pageHeight ? m.pageHeight : m.height };
  } catch { return null; }
}

async function cardHtml(p, i) {
  const delay = (i % 2 === 0) ? 'fade-up-2' : 'fade-up-3';
  let media;
  if (p.lottie) {
    media = `<div class="project-card__image" style="aspect-ratio:${p.lottieRatio};" data-lottie="${p.lottie}"></div>`;
  } else {
    const d = await dims(p.image);
    const size = d ? ` width="${d.w}" height="${d.h}"` : '';
    media = `<img class="project-card__image"${size} src="${p.image}" alt="${esc(p.alt)}" loading="lazy">`;
  }
  return `      <a href="${p.href}" class="project-card fade-up ${delay}" data-static-ink>
        <div class="project-card__ink-bg" aria-hidden="true" style="background:${p.bgColor};"></div>
        <div class="project-card__img-wrap">${media}</div>
        <div class="project-card__label" style="background:${p.bgColor};color:${p.fgColor};"><span class="project-card__label-client">${esc(p.client)}</span>${esc(p.label || p.title)}</div>
      </a>`;
}

function chipsHtml(activeTag) {
  const chips = [];
  for (const tag of Object.keys(CCD_TAG_META)) {
    chips.push(`      <a href="${CCD_TAG_HREF(tag)}" class="tag${tag === activeTag ? ' tag--active' : ''}">${esc(tag)}</a>`);
  }
  return chips.join('\n');
}

const generated = [];
for (const [tag, meta] of Object.entries(CCD_TAG_META)) {
  const file = CCD_TAG_HREF(tag);
  const url = `${SITE}/${file}`;
  const title = `${meta.title} • Clayton Cunningham Design`;
  const projects = CCD_PROJECTS.filter(p => !p.homeOnly && p.tags.includes(tag));

  const cards = [];
  for (let i = 0; i < projects.length; i++) cards.push(await cardHtml(projects[i], i));

  let out = template;
  // Head: unique title, description, canonical, og/twitter
  out = out.replace(/<title>[^<]*<\/title>/, `<title>${esc(title)}</title>`);
  out = out.replace(/(name="description" content=")[^"]*(")/, `$1${esc(meta.description)}$2`);
  out = out.replace(/(rel="canonical" href=")[^"]*(")/, `$1${url}$2`);
  out = out.replace(/(property="og:url" content=")[^"]*(")/, `$1${url}$2`);
  out = out.replace(/(property="og:title" content=")[^"]*(")/, `$1${esc(title)}$2`);
  out = out.replace(/(name="twitter:title" content=")[^"]*(")/, `$1${esc(title)}$2`);
  out = out.replace(/(property="og:description" content=")[^"]*(")/, `$1${esc(meta.description)}$2`);
  out = out.replace(/(name="twitter:description" content=")[^"]*(")/, `$1${esc(meta.description)}$2`);
  // Body: pre-rendered title, chips, grid; drop the client renderer
  out = out.replace('<h1 class="cap-title" id="cap-title"></h1>', `<h1 class="cap-title" id="cap-title">${esc(meta.title)}</h1>`);
  out = out.replace('<div class="cap-filter-chips" id="cap-filter-chips"></div>',
    `<div class="cap-filter-chips" id="cap-filter-chips">\n${chipsHtml(tag)}\n    </div>`);
  out = out.replace('<!-- Project grid, populated by JS -->\n    <div class="project-grid" id="project-grid"></div>',
    `<!-- Project grid, statically generated by generate-tag-pages.mjs -->\n    <div class="project-grid" id="project-grid">\n${cards.join('\n')}\n    </div>`);
  out = out.replace(/[ \t]*<script src="js\/capabilities\.js"><\/script>\n?/, '');
  // Strip any JSON-LD inherited from the template; inject-schema.mjs re-adds per page
  out = out.replace(/[ \t]*<script type="application\/ld\+json">.*?<\/script>\n?/s, '');

  await fs.writeFile(file, out);
  generated.push(file);
  console.log(`${file}: ${projects.length} projects`);
}
console.log(`\nGenerated ${generated.length} tag pages`);
